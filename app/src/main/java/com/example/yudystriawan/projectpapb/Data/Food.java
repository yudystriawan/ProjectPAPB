package com.example.yudystriawan.projectpapb.Data;

public class Food {
    private String desc;
    private int image;

    public Food(String desc, int image) {
        this.desc = desc;
        this.image = image;
    }

    public String getDesc() {
        return desc;
    }

    public int getImage() {
        return image;
    }
}
